    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        if( evt.getClickCount() > 1 )
        {
            String ok = JOptionPane.showInputDialog(null, "Ingrese destinario", "Confirmacion!", JOptionPane.PLAIN_MESSAGE);
            if(ok != null){
                int destinario = Jugador.search(ok);
                if(destinario != -1){
                    int x = jList1.getModel().getElementAt(jList1.getSelectedIndex()).toString().charAt(0)-49;
                    ((Saves)Jugador.getUsuarioActual().getSaves().get(x)).enemigo = Jugador.getUsuarioActual();                    
                    Jugador.jugadores.get(destinario).getSaves().add(Jugador.getUsuarioActual().getSaves().get(x));//it actually works  
                    Jugador.getUsuarioActual().getSaves().remove(x);//it actually works  
                    Jugador.reWrite();
                    new Jugar().setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "La contrase√±a es incorrecta!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

